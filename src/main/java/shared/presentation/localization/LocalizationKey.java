package shared.presentation.localization;

/**
 * It will store all the localization keys, which can be used on any UI place.
 */
public enum LocalizationKey {
    /**
     * Name of the program.
     */
    PROGRAM_NAME,
    /**
     * Application version.
     */
    VERSION,
    /**
     * Project details.
     */
    PROJECT_DETAILS,
    /**
     * About application section.
     */
    ABOUT
}
